    private void hitungBiayaHelm() {
    try {
        int jumlahHelm = Integer.parseInt(tfJumlahhelmform.getText().trim());
        int lamaSewa = Integer.parseInt(tfLamasewaform.getText().trim());
        
        int biayaHelm = 5000 * jumlahHelm * lamaSewa;
        
        tfBiayahelmform.setText(Integer.toString(biayaHelm));
    } catch (NumberFormatException ex) {
        // Tangani jika pengguna memasukkan teks yang tidak valid
        tfBiayahelmform.setText("");
        }
    }
    
