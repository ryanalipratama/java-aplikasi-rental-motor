    private void btnHitungtotalformActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungtotalformActionPerformed
        // TODO add your handling code here:
        try {
        int hargaSewaMotor = Integer.parseInt(tfHargasewaform.getText());
        int lamaSewaMotor = Integer.parseInt(tfLamasewaform.getText());
        int biayaHelm = Integer.parseInt(tfBiayahelmform.getText());
        
            if (hargaSewaMotor < 0) { // Periksa apakah input jumlah helm adalah angka positif
                JOptionPane.showMessageDialog(this, "Masukan jumlah helm yang benar!.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                tfJumlahhelmform.requestFocus();
            }else if (lamaSewaMotor < 0) { // Periksa apakah input lama sewa adalah angka positif
                JOptionPane.showMessageDialog(this, "Masukan total lama pinjam yang benar!.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                tfLamasewaform.requestFocus();
            }else if (lamaSewaMotor < 0) { // Periksa apakah input lama sewa adalah angka positif
                JOptionPane.showMessageDialog(this, "Masukan total lama pinjam yang benar!.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                tfLamasewaform.requestFocus();
            }else {
                int totalHarga = (hargaSewaMotor * lamaSewaMotor) + biayaHelm;
                tfTotalbayarform.setText(Integer.toString(totalHarga));
            }
            
        }catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Masukan lama sewa dan jumlah helm.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            tfJumlahhelmform.requestFocus();
        }
    }//GEN-LAST:event_btnHitungtotalformActionPerformed

